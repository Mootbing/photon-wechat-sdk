/**
 * WeChat run-all bot: send `/run` from another WeChat account and this example
 * replies with every outbound content path the provider currently supports.
 *
 * Run it against a live agent-wechat container:
 *
 *   pnpm build
 *   AGENT_WECHAT_TOKEN=$(cat ~/.config/agent-wechat/token) pnpm example:run-all
 *
 * Notes:
 * - MP4 files are handled by WeChat as native video messages.
 * - Native WeChat voice bubbles are not exposed by agent-wechat today; voice
 *   is delivered as an audio file.
 * - Group chats use `groups: "mentionsOnly"` by default, so mention the bot
 *   and include `/run` in the same message.
 */
import { deflateSync } from "node:zlib";
import { Spectrum, app, attachment, contact, markdown, richlink, voice } from "@spectrum-ts/core";
import type { ContentInput, Message, Space } from "@spectrum-ts/core";
import { wechat } from "photon-wechat-sdk";

const RUN_COMMAND = "/run";

const delay = (ms: number) => new Promise<void>((resolve) => setTimeout(resolve, ms));

const activeRuns = new Set<string>();

const appInstance = await Spectrum({
  providers: [
    wechat.config({
      baseUrl: process.env.AGENT_WECHAT_URL ?? "http://localhost:6174",
      token: process.env.AGENT_WECHAT_TOKEN,
      pollIntervalMs: 1500,
      sendPacingMs: 1000,
      groups: groupPolicyFromEnv(),
    }),
  ],
});

console.error("[run-all] listening for /run messages... (Ctrl-C to stop)");

for await (const [space, message] of appInstance.messages) {
  const text = message.content.type === "text" ? message.content.text.trim() : "";
  if (!text.includes(RUN_COMMAND)) continue;

  if (activeRuns.has(space.id)) {
    await space.send("run-all is already running for this chat.");
    continue;
  }

  activeRuns.add(space.id);
  try {
    await runAll(space, message);
  } finally {
    activeRuns.delete(space.id);
  }
}

async function runAll(space: Space, trigger: Message): Promise<void> {
  const runId = new Date().toISOString().replace(/[-:.TZ]/g, "").slice(0, 14);
  const results: string[] = [];

  await space.startTyping();
  await delay(400);
  await space.stopTyping();
  await trigger.read();

  await space.send(`run-all ${runId}: starting outbound content sweep.`);

  const steps: Array<{ name: string; send: () => Promise<void> }> = [
    {
      name: "reply",
      send: async () => {
        await trigger.reply(`run-all ${runId}: reply sample to your /run message.`);
      },
    },
    {
      name: "text",
      send: async () => {
        await space.send(`run-all ${runId}: plain text sample.`);
      },
    },
    {
      name: "markdown",
      send: async () => {
        await space.send(markdown(`**run-all ${runId}** markdown sample\n\n- item one\n- item two`));
      },
    },
    {
      name: "richlink",
      send: async () => {
        await space.send(richlink("https://photon.codes/spectrum"));
      },
    },
    {
      name: "app-url",
      send: async () => {
        await space.send(app("https://photon.codes/spectrum"));
      },
    },
    {
      name: "image",
      send: async () => {
        await space.send(bufferAttachment("run-all-image.png", "image/png", makePng()));
      },
    },
    {
      name: "video",
      send: async () => {
        await space.send(bufferAttachment("run-all-video.mp4", "video/mp4", makeMp4()));
      },
    },
    {
      name: "voice-file",
      send: async () => {
        await space.send(
          voice(makeWav({ durationSeconds: 1.2, frequencyHz: 440 }), {
            duration: 1.2,
            mimeType: "audio/wav",
            name: "run-all-voice.wav",
          }),
        );
      },
    },
    {
      name: "generic-file",
      send: async () => {
        await space.send(
          bufferAttachment(
            "run-all-file.json",
            "application/json",
            Buffer.from(
              JSON.stringify(
                {
                  runId,
                  generatedBy: "examples/run-all-bot.ts",
                  purpose: "photon-wechat-sdk outbound file smoke test",
                },
                null,
                2,
              ),
              "utf8",
            ),
          ),
        );
      },
    },
    {
      name: "contact-vcard",
      send: async () => {
        await space.send(
          contact({
            name: { formatted: "Photon WeChat Run All" },
            emails: [{ value: "run-all@example.invalid", type: "work" }],
            org: {
              name: "photon-wechat-sdk",
              title: "Synthetic test contact",
            },
            urls: ["https://photon.codes/spectrum"],
            note: `Generated by examples/run-all-bot.ts for ${runId}.`,
          }),
        );
      },
    },
  ];

  for (const step of steps) {
    try {
      console.error(`[run-all] ${runId} sending ${step.name} to ${space.id}`);
      await step.send();
      results.push(`${step.name}=ok`);
    } catch (error) {
      const message = errorMessage(error);
      console.error(`[run-all] ${runId} ${step.name} failed: ${message}`);
      results.push(`${step.name}=fail`);
      await space.send(`run-all ${runId}: ${step.name} failed: ${message}`);
    }
  }

  await space.send(
    [
      `run-all ${runId}: complete.`,
      `Results: ${results.join(", ")}`,
      "Native WeChat voice bubbles, reactions, polls, edit, unsend, rename, and avatar are not currently supported by this provider.",
    ].join("\n"),
  );
}

function bufferAttachment(name: string, mimeType: string, bytes: Buffer): ContentInput {
  return attachment(bytes, { mimeType, name });
}

function groupPolicyFromEnv(): "exclude" | "include" | "mentionsOnly" {
  const value = process.env.WECHAT_GROUPS;
  if (value === "exclude" || value === "include" || value === "mentionsOnly") {
    return value;
  }
  return "mentionsOnly";
}

function errorMessage(error: unknown): string {
  return error instanceof Error ? error.message : String(error);
}

function makePng(): Buffer {
  const width = 64;
  const height = 64;
  const rows: Buffer[] = [];

  for (let y = 0; y < height; y += 1) {
    const row = Buffer.alloc(1 + width * 3);
    row[0] = 0;
    for (let x = 0; x < width; x += 1) {
      const offset = 1 + x * 3;
      row[offset] = (30 + x * 3) % 256;
      row[offset + 1] = (80 + y * 2) % 256;
      row[offset + 2] = (220 - x + y) % 256;
    }
    rows.push(row);
  }

  const ihdr = Buffer.alloc(13);
  ihdr.writeUInt32BE(width, 0);
  ihdr.writeUInt32BE(height, 4);
  ihdr[8] = 8;
  ihdr[9] = 2;
  ihdr[10] = 0;
  ihdr[11] = 0;
  ihdr[12] = 0;

  return Buffer.concat([
    Buffer.from([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]),
    pngChunk("IHDR", ihdr),
    pngChunk("IDAT", deflateSync(Buffer.concat(rows))),
    pngChunk("IEND", Buffer.alloc(0)),
  ]);
}

